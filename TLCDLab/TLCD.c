int main()
{
    int fbfd , state , ret;
    struct fb_var_screeninfo fbvar;

    int inputBtn[12] = { 0 };
    fbfd = open(FBDEVFILE, O_RDWR);
    if (fbfd < 0)
    {
        perror("fbdev open");
        exit(1);
    }
    ret = ioctl(fbfd, FBIOGET_VSCREENINFO, &fbvar);
    if (ret < 0)
    {
        perror("fbdev ioctl");
        exit(1);
    }
    if (fbvar.bits_per_pixel != 16)
    {
        fprintf(stderr, "bpp is not 16\n");
        exit(1);
    }

    wiringPiSetup();
    setUpFont();
    initialize_textlcd();

    for (;;) {
        if (digitalRead(BTN1) ||
            digitalRead(BTN2) ||
            digitalRead(BTN3) ||
            digitalRead(BTN4) ||
            digitalRead(BTN5) ||
            digitalRead(BTN6) ||
            digitalRead(BTN7) ||
            digitalRead(BTN8) ||
            digitalRead(BTN9) ||
            !digitalRead(BTNDEL) ||
            !digitalRead(BTNLft) ||
            !digitalRead(BTNRgt)) {
            if (state == 0) {
                if (digitalRead(BTN1) == 1) {
                    inputBtn[0] += 1;
                }
                else if (digitalRead(BTN2) == 1) {
                    inputBtn[1] += 1;
                }
                else if (digitalRead(BTN3) == 1) {
                    inputBtn[2] += 1;
                }
                else if (digitalRead(BTN4) == 1) {
                    inputBtn[3] += 1;
                }
                else if (digitalRead(BTN5) == 1) {
                    inputBtn[4] += 1;
                }
                else if (digitalRead(BTN6) == 1) {
                    inputBtn[5] += 1;
                }
                else if (digitalRead(BTN7) == 1) {
                    inputBtn[6] += 1;
                }
                else if (digitalRead(BTN8) == 1) {
                    inputBtn[7] += 1;
                }
                else if (digitalRead(BTN9) == 1) {
                    inputBtn[8] += 1;
                }
                else if (!digitalRead(BTNDEL) == 1) {
                    inputBtn[9] += 1;
                }
                else if (!digitalRead(BTNLft) == 1) {
                    inputBtn[10] += 1;
                }
                else if (!digitalRead(BTNRgt) == 1) {
                    inputBtn[11] += 1;
                }
                printf("%s\n" , outputStr);
                delay(10);
                state = 1;
            }
        }
        else if (digitalRead(BTN1) == 0 &&
            digitalRead(BTN2) == 0 &&
            digitalRead(BTN3) == 0 &&
            digitalRead(BTN4) == 0 &&
            digitalRead(BTN5) == 0 &&
            digitalRead(BTN6) == 0 &&
            digitalRead(BTN7) == 0 &&
            digitalRead(BTN8) == 0 &&
            digitalRead(BTN9) == 0 &&
            !digitalRead(BTNDEL) == 0 &&
            !digitalRead(BTNLft) == 0 &&
            !digitalRead(BTNRgt) == 0) {
            if (state == 1) {
                delay(10);
                state = 0;
            }
        }
    }

	return 0;
}


void initialize_textlcd() {
    pinMode(BTN7, INPUT);
    pinMode(BTN8, INPUT);
    pinMode(BTN9, INPUT);
    pinMode(BTN4, INPUT);
    pinMode(BTN5, INPUT);
    pinMode(BTN6, INPUT);
    pinMode(BTN1, INPUT);
    pinMode(BTN2, INPUT);
    pinMode(BTN3, INPUT);
    pinMode(BTNDEL, INPUT);
    pinMode(BTNLft, INPUT);
    pinMode(BTNRgt, INPUT);

    pullUpDnControl(BTN7, PUD_DOWN);
    pullUpDnControl(BTN8, PUD_DOWN);
    pullUpDnControl(BTN9, PUD_DOWN);
    pullUpDnControl(BTN4, PUD_DOWN);
    pullUpDnControl(BTN5, PUD_DOWN);
    pullUpDnControl(BTN6, PUD_DOWN);
    pullUpDnControl(BTN1, PUD_DOWN);
    pullUpDnControl(BTN2, PUD_DOWN);
    pullUpDnControl(BTN3, PUD_DOWN);
    pullUpDnControl(BTNDEL, PUD_UP);
    pullUpDnControl(BTNLft, PUD_UP);
    pullUpDnControl(BTNRgt, PUD_UP);

    delay(2);
}

void copy(int array1[24][24], int array2[24][24])
{
    int* p1, * endp1;
    int* p2;
    p1 = &array1[0][0];
    p2 = &array2[0][0];
    endp1 = &array1[24 - 1][24 - 1];
    while (p1 <= endp1)
    {
        *p2 = *p1;
        p1++; p2++;
    }
}

